// --------------------------------------------------
// 화살표 함수 표현식 (Arrow Function Expression)
// --------------------------------------------------

// 함수 만드는 방법 3가지

// 1. 함수 선언
function showMeTheMoney() {
  console.log('쇼미더머니');
}
showMeTheMoney();

// 2. 함수 표현식
let lookAtMe = function () {
  console.log('룩앳미');
};
lookAtMe();

// 3. 화살표 함수 표현식
let readABook = () => {
  console.log('책 읽자');
};
readABook();
